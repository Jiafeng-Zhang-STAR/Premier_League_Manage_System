    public void initializeInfo(){
        String player_Name = this.person.getUsername();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            String url = "jdbc:mysql://localhost:3306/premierleague?zeroDateTimeBehavior=CONVERT_TO_NULL";
            String username = "root";
            String password = "150823";
            Connection connection = DriverManager.getConnection(url, username, password);
            Statement statement = connection.createStatement();
            String sql = "SELECT * FROM system_user_info WHERE username=\'"+player_Name+"\'";
            ResultSet resultSet = statement.executeQuery(sql);
            while(resultSet.next()){
                this.usernameLabel.setText(String.valueOf(resultSet.getObject("username")));
                this.clubLabel.setText(String.valueOf(resultSet.getObject("club")));
                this.nameLabel.setText(String.valueOf(resultSet.getObject("name")));
                if(Integer.parseInt(String.valueOf(resultSet.getObject("gender")))==1){
                    this.genderLabel.setText("male");
                }else{
                    this.genderLabel.setText("female");
                }
                this.ageLabel.setText(String.valueOf(resultSet.getObject("age")));
                this.NationLabel.setText(String.valueOf(resultSet.getObject("nation")));
                this.addressLabel.setText(String.valueOf(resultSet.getObject("address")));
                this.zipLabel.setText(String.valueOf(resultSet.getObject("zip")));
            }
            
            resultSet.close();
            statement.close();
            connection.close();
        }catch(ClassNotFoundException | SQLException e){
        }
    }

